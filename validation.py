# 11. CKA
# ============================================================

def gram(x):

    return x @ x.T

def center(g):

    n = g.size(0)

    H = torch.eye(n, device=g.device) \
        - torch.ones(n,n,device=g.device)/n

    return H @ g @ H

def cka(x, y):

    x = x.flatten(1)
    y = y.flatten(1)

    K = center(gram(x))
    L = center(gram(y))

    return (K*L).sum() / (
        torch.norm(K)*torch.norm(L)
    )

# ============================================================
# 12. COMPUTE CKA MATRIX
# ============================================================

def compute_cka_matrix(model, loader):

    model.eval()

    images, _ = next(iter(loader))

    images = images.to(device)

    _, acts = model(images, return_features=True)

    acts = [a.mean(dim=[2,3]) for a in acts]

    n = len(acts)

    mat = torch.zeros(n,n)

    for i in range(n):

        for j in range(n):

            mat[i,j] = cka(acts[i], acts[j]).item()

    return mat

