########################################
# Settings
########################################

IMG_SIZE = 32
DATASET_SIZE = 5000
BATCH_SIZE = 128
EPOCHS = 4

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")

########################################
# Seed
########################################

def set_seed(seed=42):
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)

set_seed()

########################################
# Stimuli Generation
########################################

def generate_stimuli():

    freqs=[2,4,8]
    thetas=np.arange(0,180,20)
    phases=[0,0.5]

    x=np.arange(IMG_SIZE)
    y=np.arange(IMG_SIZE)
    xv,yv=np.meshgrid(x,y)

    stimuli=[]

    for f in freqs:
        for t in thetas:
            theta=np.deg2rad(t)
            for p in phases:
                img=np.sin(
                    2*np.pi*f*(xv*np.cos(theta)+yv*np.sin(theta))/IMG_SIZE + p
                )
                stimuli.append(img)

    stimuli=np.array(stimuli).astype(np.float32)
    return torch.tensor(stimuli).unsqueeze(1)

stimuli = generate_stimuli().to(device)

