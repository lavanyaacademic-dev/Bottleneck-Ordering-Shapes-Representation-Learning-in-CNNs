########################################
# Training
########################################

def train(model):

    opt=torch.optim.Adam(model.parameters(),lr=1e-3)
    loss_fn=nn.CrossEntropyLoss()

    model.train()

    for epoch in range(EPOCHS):

        total=0

        for x,y in train_loader:

            x,y=x.to(device),y.to(device)

            opt.zero_grad()

            out,_,_,_=model(x)

            loss=loss_fn(out,y)

            loss.backward()

            opt.step()

            total+=loss.item()

        print("Epoch",epoch+1,"Loss:",total)

