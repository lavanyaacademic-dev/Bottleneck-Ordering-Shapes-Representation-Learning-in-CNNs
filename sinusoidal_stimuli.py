# Model
########################################

class MSBCNN(nn.Module):

    def __init__(self,width):
        super().__init__()

        c1=width
        c2=width*2
        c3=width*4

        self.stage1=nn.Sequential(
            nn.Conv2d(1,16,3,padding=1),
            nn.ReLU(),
            nn.Conv2d(16,c1,3,padding=1),
            nn.ReLU()
        )

        self.stage2=nn.Sequential(
            nn.Conv2d(c1,32,3,padding=1),
            nn.ReLU(),
            nn.Conv2d(32,c2,3,padding=1),
            nn.ReLU()
        )

        self.stage3=nn.Sequential(
            nn.Conv2d(c2,64,3,padding=1),
            nn.ReLU(),
            nn.Conv2d(64,c3,3,padding=1),
            nn.ReLU()
        )

        self.pool=nn.AdaptiveAvgPool2d(1)
        self.fc=nn.Linear(c3,3)

    def forward(self,x):

        s1=self.stage1(x)
        s2=self.stage2(s1)
        s3=self.stage3(s2)

        out=self.pool(s3).view(x.size(0),-1)
        out=self.fc(out)

        return out,s1,s2,s3

########################################
# Training
########################################

def train(model):

    opt=torch.optim.Adam(model.parameters(),lr=1e-3)
    loss_fn=nn.CrossEntropyLoss()

    model.train()

    for epoch in range(EPOCHS):

        total=0

        for x,y in train_loader:
            x,y=x.to(device),y.to(device)

            opt.zero_grad()
            out,_,_,_=model(x)
            loss=loss_fn(out,y)
            loss.backward()
            opt.step()

            total+=loss.item()

        print("Epoch",epoch+1,"Loss:",total)

