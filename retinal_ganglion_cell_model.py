set_seed()

########################################
# Retinal Ganglion Model (DoG)
########################################

def dog_kernel(size=9, sigma_c=1.5, sigma_s=3):

    ax = np.linspace(-(size//2), size//2, size)
    xx, yy = np.meshgrid(ax, ax)

    center = np.exp(-(xx**2 + yy**2)/(2*sigma_c**2))
    surround = np.exp(-(xx**2 + yy**2)/(2*sigma_s**2))

    dog = center - surround
    dog = dog / np.sum(np.abs(dog))

    kernel = torch.tensor(dog).float().unsqueeze(0).unsqueeze(0)

    return kernel

dog = dog_kernel().to(device)

def retinal_layer(x):
    return F.conv2d(x, dog, padding=4)

########################################
# Stimulus Generation
########################################

def generate_stimuli():

    freqs=[2,4,8]
    thetas=np.arange(0,180,20)
    phases=[0,0.5]

    x=np.arange(IMG_SIZE)
    y=np.arange(IMG_SIZE)
    xv,yv=np.meshgrid(x,y)

    stimuli=[]

    for f in freqs:
        for t in thetas:
            theta=np.deg2rad(t)
            for p in phases:
                img=np.sin(
                    2*np.pi*f*(xv*np.cos(theta)+yv*np.sin(theta))/IMG_SIZE + p
                )
                stimuli.append(img)

    stimuli=np.array(stimuli).astype(np.float32)
    stimuli=torch.tensor(stimuli).unsqueeze(1)

    return stimuli

stimuli = generate_stimuli().to(device)

########################################
# Retinal Responses
########################################

with torch.no_grad():
    stimuli_retina = retinal_layer(stimuli)

