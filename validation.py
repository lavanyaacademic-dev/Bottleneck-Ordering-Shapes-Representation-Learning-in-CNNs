def visualize_features(model, loader, title):

    model.eval()

    images, _ = next(iter(loader))

    images = images.to(device)

    _, acts = model(images[:1], return_features=True)

    for i, act in enumerate(acts):

        fmap = act[0].detach().cpu()

        plt.figure(figsize=(12,2))

        for j in range(min(8, fmap.shape[0])):

            plt.subplot(1,8,j+1)

            plt.imshow(fmap[j], cmap='viridis')

            plt.axis('off')

        plt.suptitle(f"{title} - Layer {i+1}")

        plt.show()

