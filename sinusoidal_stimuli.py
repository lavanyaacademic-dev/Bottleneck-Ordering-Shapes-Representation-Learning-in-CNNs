# Opponency
########################################

def opponency(stage):

    acts=stage.mean(dim=(2,3)).cpu().numpy()

    baseline=acts.mean(axis=0)
    delta=0.1*(acts.max(axis=0)-acts.min(axis=0))

    opp=((acts.max(axis=0)>baseline+delta) &
         (acts.min(axis=0)<baseline-delta))

    return opp.mean()

