# 7. BOTTLENECK ORDERED RESNET18
# ============================================================

class BottleneckResNet18(nn.Module):

    def __init__(self, channels, num_classes=10):

        super().__init__()

        c1, c2, c3, c4 = channels

        self.in_channels = c1

        self.conv1 = nn.Conv2d(
            3,
            c1,
            kernel_size=3,
            stride=1,
            padding=1,
            bias=False
        )

        self.bn1 = nn.BatchNorm2d(c1)

        self.layer1 = self._make_layer(c1, 2, stride=1)
        self.layer2 = self._make_layer(c2, 2, stride=2)
        self.layer3 = self._make_layer(c3, 2, stride=2)
        self.layer4 = self._make_layer(c4, 2, stride=2)

        self.pool = nn.AdaptiveAvgPool2d((1,1))

        self.fc = nn.Linear(c4, num_classes)

    def _make_layer(self, out_channels, blocks, stride):

        layers = []

        strides = [stride] + [1]*(blocks-1)

        for s in strides:

            layers.append(
                BasicBlock(
                    self.in_channels,
                    out_channels,
                    s
                )
            )

            self.in_channels = out_channels

        return nn.Sequential(*layers)

    def forward(self, x, return_features=False):

        feats = []

        out = torch.relu(self.bn1(self.conv1(x)))
        feats.append(out)

        out = self.layer1(out)
        feats.append(out)

        out = self.layer2(out)
        feats.append(out)

        out = self.layer3(out)
        feats.append(out)

        out = self.layer4(out)
        feats.append(out)

        pooled = self.pool(out)

        pooled = pooled.view(pooled.size(0), -1)

        logits = self.fc(pooled)

        if return_features:
            return logits, feats

        return logits

