def pca_analysis(model, loader, title):

    model.eval()

    images, _ = next(iter(loader))

    images = images.to(device)

    _, acts = model(images, return_features=True)

    feat = acts[-1]

    feat = feat.mean(dim=[2,3])

    feat = feat.detach().cpu().numpy()

    pca = PCA(n_components=2)

    reduced = pca.fit_transform(feat)

    plt.figure(figsize=(6,6))

    plt.scatter(
        reduced[:,0],
        reduced[:,1],
        s=10
    )

    plt.title(f"PCA Representation: {title}")

    plt.xlabel("PC1")
    plt.ylabel("PC2")

    plt.show()

